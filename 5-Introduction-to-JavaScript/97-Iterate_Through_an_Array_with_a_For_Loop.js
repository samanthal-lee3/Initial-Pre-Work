// js can iterate through contents of an array
// for loop can be called on an array to execute a function for each element in the array

// Example
var ourArr = [ 9, 10, 11, 12];
var ourTotal = 0;

for (var i = 0; i < ourArr.length; i++) {
  ourTotal += ourArr[i];
}


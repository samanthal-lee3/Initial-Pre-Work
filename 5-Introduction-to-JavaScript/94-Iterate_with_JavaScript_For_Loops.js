// for loops runs for a specific number of times
// declared with 3 optional expressions- initialization, condition, final-expression
// initialization- executed one time before the loop starts; defines variable
// condition- evaluated at the beginning of every loop and will continue as long as it is true
// final-expression is executed at end of loop iteration, prior to the next condition

// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

